    private void darDeBajaButtonMouseClicked(java.awt.event.MouseEvent evt) {                                             
        if(usuarioSeleccioando == null){
            usuarioSeleccioando = gestorUsuarios.buscarUsuarioAdmin(seleccionarUsuario.getText());
            if(usuarioSeleccioando == null){
                jLabel1.setText("Nombre Incorrecto");
            } else {
                usuarioSeleccionado.setText(usuarioSeleccioando.getNombre());
                if(usuarioSeleccioando.isCuentaActiva()){
                    jLabel1.setText("Dar de baja");
                } else {
                    jLabel1.setText("Habilitar");
                }
            }
        } else {
            if(usuarioSeleccioando.isCuentaActiva()){
                usuarioSeleccioando.setCuentaActiva(false);
                gestorUsuarios.cargarUsuarioJson(usuarioSeleccioando);
                usuarioSeleccioando = null;
                jLabel1.setText("Confirmar Busqueda");
            } else {
                usuarioSeleccioando.setCuentaActiva(true);
                gestorUsuarios.cargarUsuarioJson(usuarioSeleccioando);
                usuarioSeleccioando = null;
                jLabel1.setText("Confirmar Busqueda");
            }
        }
    }                                            

