    public int cargarUsuariosLista(int j){
        ArrayList<JLabel> labels = iteradorLabelUsuario();
        Collection<Usuario> it = gestorUsuarios.getHashMap().values();
        Iterator<Usuario> ite = it.iterator();
        int i = 0, k = 0;

        if(j!=0){
            while (k < j && ite.hasNext()) {
                ite.next();
                k++;
            }
        }

        while (i < labels.size() && ite.hasNext()) {
            Usuario aux = ite.next();
            labels.get(i).setText(aux.getNombre());
            i++;
            j++;
        }

        while(i < labels.size()){
            labels.get(i).setText("------");
            i++;
            j=0;
        }

        return j;

    }


